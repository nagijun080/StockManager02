package com.example.stockmanagerforandroid;

public class CustomDialogInCart {

	private Integer imageId;
	private String itemId;
	private String itemName;
	private String itemData;
	private String itemNum;
	private String itemUnitPrice;
	private String itemMinTotal;
	
	public void setImageId(Integer imageId_) {
		imageId = imageId_;
	}
	
	public Integer getImageId() {
		return imageId;
	}
	
	public void setItemId(String itemId_) {
		itemId = itemId_;
	}
	
	public String getItemId() {
		return itemId;
	}
	
	public void setItemName(String itemName_) {
		itemName = itemName_;
	}
	
	public String getItemName() {
		return itemName;
	}
	
	public void setItemData(String itemData_) {
		itemData = itemData_;
	}
	
	public String getItemData() {
		return itemData;
	}
	
	public void setItemNum(String itemNum_) {
		itemNum = itemNum_;
	}
	
	public String getItemNum() {
		return itemNum;
	}
	
	public void setItemUnitPrice(String itemUnitPrice_) {
		itemUnitPrice = itemUnitPrice_;
	}
	
	public String getItemUnitPrice() {
		return itemUnitPrice;
	}
	
	public void setItemMinTotal(String itemMinTotal_) {
		itemMinTotal = itemMinTotal_;
	}
	
	public String getItemMinTotal() {
		return itemMinTotal;
	}
}
